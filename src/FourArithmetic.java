public class FourArithmetic {
    public static void main(String[] args) {
        //두개의 정수는 12와 2 이고 변수명은 A, B
        int a =12 ;
        int b = 2 ;
        System.out.println( "a+b = " + (a+b) );
        System.out.println( "a-b = " + (a-b) );
        System.out.println( "a*b = " + a*b );
        System.out.println( "a/b = " + a/b );

    }
}
