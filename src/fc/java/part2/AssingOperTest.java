package fc.java.part2;

public class AssingOperTest {
    public static void main(String[] args) {
        // 산술 대입 연산자를 이해하자.
        int a= 1 ;
        a++ ;
        System.out.println(a);
        ++a ;
        System.out.println(a);
        a = a+1 ;
        System.out.println(a);
        a+= 1 ;
        System.out.println(a);

    }
}
